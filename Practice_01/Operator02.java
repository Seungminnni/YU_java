package Practice_01;

public class Operator02 {
    public static void main(String[] args) {
        float a = 0.025f;
        float b = 1.125f;
        System.out.println("a == b = " + (a == b));  // 0.025 == 1.125 = false
        System.out.println("a != b = " + (a != b));  // 0.025 != 1.125 = true
        System.out.println("a < b = " + (a < b));    // 0.025 < 1.125 = true
        System.out.println("a > b = " + (a > b));    // 0.025 > 1.125 = false
        System.out.println("a <= b = " + (a <= b));  // 0.025 <= 1.125 = true
        System.out.println("a >= b = " + (a >= b));  // 0.025 >= 1.125 = false

    }
    
}
